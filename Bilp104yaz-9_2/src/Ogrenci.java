
public class Ogrenci {
	
	
	private String ad;
	private String soyad;
	private int numara;
	
	public void setAd(String ad) {
		this.ad=ad;
		
	}
	
	public void setSoyad(String soyad) {
		this.soyad=soyad;
		
	}
	
	public void setNumara(int numara) {
		this.numara=numara;
		
	}
	
	public String getAd() {
		return this.ad;
		
	}
	
	public String getSoyad() {
		return this.soyad;
		
	}
	
	public int getNumara() {
		return this.numara;
		
	}
	

}
